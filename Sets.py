# Creating a Set
aus_cities = {"Sydney", "Melbourne", "Adelide", "Brisbane"}
print(aus_cities)

for cities in aus_cities:
    print(cities)

# Converting a List into a set using set constructor
print("-"  * 60)
aus_suburbs = set(["Parramatta" , "Chatswood", "WestMead", "Toongabie"])
print(aus_suburbs)

for suburbs in aus_suburbs:
    print(suburbs)
