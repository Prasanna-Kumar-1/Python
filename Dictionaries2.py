# Implementation of Dictionaries Concept
cities = {
    1: 'Sydney',
    2: 'Melbourne',
    3: 'Brisbane',
    4: 'Adelide'
}
print(cities[1])
print(cities[4])

aus_cities = {"NSW": "Sydney", "Victoria": "Melbourne", "Queensland": "Brisbane", "WA": "Perth"}
print(aus_cities["NSW"])
print(aus_cities["Victoria"])
