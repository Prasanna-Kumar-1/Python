# List indexing
my_list1 = [1, 2, 3]
print(my_list1[0])
print(my_list1[1])
print(my_list1[2])

# List Slicing
my_list2 = ["one", "two", "three"]
print(my_list2[1:])
print(my_list2[::-1])
print(my_list2[:2])
print(my_list2[:3])

# Concatenating the lists

my_list3 = ["four", "five", "six"]
my_list4 = ["seven", "eight", "nine"]

print(my_list3 + my_list4)
