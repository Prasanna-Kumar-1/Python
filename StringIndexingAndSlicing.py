my_string = "My Australian Journey"

# String Indexing
print(my_string[0])
print(my_string[3])

# String Slicing - Slicing from one particular position till the end
print(my_string[3:])

# String Slicing - Slicing from beginning to one particular position
# IMPORTANT : the 'stop position' is always 'Up to and Not including'
print(my_string[:14])

# String Slicing - extracting a sub string
print(my_string[3:14])

# String Slicing - Start - Stop - Step Size
print(my_string[3:14:2])

# String Reversing
print(my_string[::-1])