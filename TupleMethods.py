my_tuple = ("Sydney", "is", "beautiful", "city", "to live", 2150, "Sydney")

# To count the number of occurrences of an element
print(my_tuple.count('Sydney'))

# To find the index of an element
print(my_tuple.index('to live'))

# If there are duplicate elements, index() returns the position of first occurrence
print(my_tuple.index('Sydney'))
