# # Start Value, Stop Value and Step Value. Ranges include "Up to and NOT including the Stop Value
for i in range(0, 10, 2):
    print("The value of i is : {} ".format(i))

# Start Value, Stop Value and negative Step Value. Ranges include "Up to and NOT including the Stop Value
for i in range(10, 0, -2):
    print("The value of i is : {} ".format(i))